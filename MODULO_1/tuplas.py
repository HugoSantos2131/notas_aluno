#Tupla é uma coleçao ordenada e imutavel
#Ordenada = tem ordem
#Imutavel = não pode ser mudada

# Criando tupla
tupla = (0, 1, 2, 3, 4, 5)
print(tupla[1])

#Método count: Retorna a quantidade de vezes que um elemento aparece
contagem = tupla.count(2)
print("Quantidade de vezes que o número dois apaerece: ", contagem)

#Método index: Identifica o indisse
indice = tupla.index(3)
